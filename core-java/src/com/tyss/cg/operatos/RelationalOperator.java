package com.tyss.cg.operatos;

public class RelationalOperator {
	public static void main(String[] args) {
		int i = 97;
		int j = 45;
		System.out.println("i is lesser than j: " + (i < j));
		System.out.println("i is greater than j: " + (i > j));
		System.out.println("i is lesser than or equal to j: " + (i <= j));
		System.out.println("i is greater than or equal to j: " + (i >= j));
		System.out.println("i is not equal to j: " + (i != j));
		System.out.println("i is  equal to j: " + (i == j));
	}

}
